window.getI18n = function (key, lang) {
  lang = lang || window.currentLang || "en";
  const parts = key.split(".");
  let obj = window.I18N[lang] || window.I18N.en;
  for (let i = 0; i < parts.length; i++) {
    if (!obj) return null;
    obj = obj[parts[i]];
  }
  return obj != null ? obj : window.getI18n(key, "en");
};

window.applyLanguage = function (lang) {
  if (!window.I18N[lang]) lang = "en";
  window.currentLang = lang;
  localStorage.setItem("lang", lang);
  document.documentElement.lang = lang === "am" ? "am" : lang === "om" ? "om" : "en";

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    const val = window.getI18n(key, lang);
    if (val == null) return;
    if (el.hasAttribute("data-i18n-placeholder")) {
      el.placeholder = val;
    } else if (el.hasAttribute("data-i18n-html")) {
      el.innerHTML = val;
    } else if (el.hasAttribute("data-i18n-aria")) {
      el.setAttribute("aria-label", val);
    } else {
      el.textContent = val;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
    if (el.hasAttribute("data-i18n")) return;
    const key = el.getAttribute("data-i18n-html");
    const val = window.getI18n(key, lang);
    if (val != null) el.innerHTML = val;
  });

  document.querySelectorAll("[data-i18n-msg]").forEach(function (el) {
    const key = el.getAttribute("data-i18n-msg");
    const val = window.getI18n(key, lang);
    if (val != null) el.setAttribute("data-msg", val);
  });

  document.querySelectorAll(".lang-btn").forEach(function (btn) {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
    btn.setAttribute("aria-pressed", btn.getAttribute("data-lang") === lang);
  });

  const meta = window.getI18n("meta", lang);
  if (meta) {
    document.title = meta.title;
    const desc = document.querySelector('meta[name="description"]');
    if (desc) desc.setAttribute("content", meta.description);
  }

  document.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang: lang } }));
};
